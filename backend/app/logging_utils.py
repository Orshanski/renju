"""Безопасное логирование: гасим log injection (CWE-117) от пользовательских данных.

Подсунутый в имя/координаты/сообщение CR/LF подделал бы лишние строки лога, когда
его читает построчный потребитель (journald/ELK). safe() экранирует CR/LF и режет
длину. Применять на КАЖДОМ log.*, где аргумент из пользовательских данных.
"""

_LF_ESCAPE = {0x0A: "\\n", 0x0D: "\\r"}


def safe(value: object, maxlen: int = 200) -> str:
    """Обезвредить произвольное значение для строки лога: экранировать CR/LF +
    обрезать до maxlen (защита от флуда). Экранирование ДО обрезки."""
    return str(value).translate(_LF_ESCAPE)[:maxlen]
